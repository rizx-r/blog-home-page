<template>
  <div
    class="hitokoto cards"
    v-show="!store.musicOpenState"
    @mouseenter="openMusicShow = true"
    @mouseleave="openMusicShow = false"
    @click.stop
  >
    <!-- 打开音乐面板 -->
    <Transition name="el-fade-in-linear">
      <div
        class="open-music"
        v-show="openMusicShow && store.musicIsOk"
        @click="store.musicOpenState = true"
      >
        <music-menu theme="filled" size="18" fill="#efefef" />
        <span>打开音乐播放器</span>
      </div>
    </Transition>
    <!-- 一言内容 -->
    <Transition name="el-fade-in-linear" mode="out-in">
      <div :key="hitokotoData.text" class="content" @click="updateHitokoto">
        <span class="text">{{ hitokotoData.text }}</span>
        <span class="from">-「&nbsp;{{ hitokotoData.from }}&nbsp;」</span>
      </div>
    </Transition>
  </div>
</template>

<script setup>
import { MusicMenu } from "@icon-park/vue-next";
import { mainStore } from "@/store";
import hitokotoList from "@/data/hitokoto.json";

const store = mainStore();

// 开启音乐面板按钮显隐
const openMusicShow = ref(false);

const hitokotoData = reactive({
  text: "",
  from: "公告",
});
const currentIndex = ref(0);
let timer = null;

const setFromList = (index) => {
  const item = hitokotoList[index % hitokotoList.length];
  hitokotoData.text = item.hitokoto || "";
  hitokotoData.from = item.from || "公告";
};

const updateHitokoto = () => {
  currentIndex.value = (currentIndex.value + 1) % hitokotoList.length;
  setFromList(currentIndex.value);
};

onMounted(() => {
  setFromList(currentIndex.value);
  timer = setInterval(() => {
    updateHitokoto();
  }, 6000);
});
onUnmounted(() => {
  if (timer) clearInterval(timer);
});
</script>

